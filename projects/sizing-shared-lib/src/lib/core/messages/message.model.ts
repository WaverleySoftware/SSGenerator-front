/**
 * The class used to display generic messages on a page.
 */
export class Message {

  /**
   * Gets or sets the messageKey
   */
  messageKey: string;

  /**
   * Gets or sets the value.
   */
  value: number;

  /**
   * Gets or sets the unitId.
   */
  unitKey: string;

  /**
   * Gets or sets the severity.
   */
  severity: number;

  displayValue: string;
}

/**
 * Defines the unit model class.
 */
export class Unit {
  /**
   * Gets or sets the Id.
   */
  id: number;

  /**
   * Gets or sets the name.
   */
  name: string;

  units: string;

  /**
   * Gets or sets the unit type.
   */
  unitType: string;

  /**
  * Gets or sets the  type.
  */
  type: string;

  /**
   * Gets or sets the master text key.
   */
  masterTextKey: string;
}

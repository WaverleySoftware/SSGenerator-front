export class ProductSelection {
  /**
   * Gets or sets the product names for the product range.
   */
  productNames: string[];

  /**
   * Gets or sets the product range Id.
   */
  productRangeId: number;

  /**
   * Gets or sets the product range name.
   */
  productRangeName: string;

  /**
   * Gets or sets the module group Id.
   */
  moduleGroupId: number;

  /**
   * Gets or sets the product type Id.
   */
  productTypeId: number;

  /**
   * Gets or sets a value indicating whether or not the current product range is selected.
   */
  isSelected: boolean;
}

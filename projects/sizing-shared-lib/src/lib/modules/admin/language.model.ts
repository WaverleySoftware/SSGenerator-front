/**
 * The language model class.
 */
export class Language {

  /**
   * Gets or sets the ISO Name of the language.
   */
  isoName: string;

  /**
   * Gets or sets the name of the language.
   */
  name: string;

  /**
 * Gets or sets the masterTextKey of the language.
 */
  masterTextKey: string;
}
